package filter

import (
	"github.com/stretchr/testify/assert"
	"testing"
)

func Test_CrlfFilter(t *testing.T) {
	// \r\r 和 \r\n
	src := []byte{0x0D, 0x32, 0x0A, 0x33, 0x35, 0x0D, 0x0D, 0x36, 0x0D, 0x0A}
	// 仅清除\r
	dst := crlfFilter(src, CrTrimModeOnlyCr)
	expect := []byte{0x32, 0x0A, 0x33, 0x35, 0x36, 0x0A}
	assert.Equal(t, expect, dst)

	// 清除\r及其左侧的当前行内容
	src = []byte{0x0D, 0x32, 0x0A, 0x33, 0x35, 0x0D, 0x0D, 0x36, 0x0D, 0x0A}
	dst = crlfFilter(src, CrTrimModeBeginOfLine)
	expect = []byte{0x32, 0x0A, 0x36, 0x0A}
	assert.Equal(t, expect, dst)

	// 特殊处理
	// H3C \r\r\n 和 \r\r\n\NUL
	src = []byte{0x2A, 0x0D, 0x0D, 0x0A, 0x0D, 0x0D, 0x0A, 0x00, 0x3C, 0x48, 0x4C, 0x57, 0x4A, 0x52, 0x53, 0x57, 0x2D, 0x41, 0x4D, 0x34, 0x31, 0x2D, 0x33, 0x30, 0x32, 0x3E, 0x0D, 0x0D, 0x0A}
	expect = []byte{0x2A, 0x0A, 0x0A, 0x3C, 0x48, 0x4C, 0x57, 0x4A, 0x52, 0x53, 0x57, 0x2D, 0x41, 0x4D, 0x34, 0x31, 0x2D, 0x33, 0x30, 0x32, 0x3E, 0x0A}
	dst = crlfFilter(src, CrTrimModeBeginOfLine)
	assert.Equal(t, expect, dst)
}
